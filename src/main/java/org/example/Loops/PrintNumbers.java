package org.example.Loops;
//Write a program that uses a for loop to print numbers from 1 to 10.
public class PrintNumbers {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {

            System.out.print(i);
        }
        for (int i = 1; i <= 10; i++) {
            System.out.print(i + " ");

        }
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }
}